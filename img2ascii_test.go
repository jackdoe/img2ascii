package img2ascii

import (
	"encoding/base64"
	"image"
	"strings"
	"testing"
)

func TestConvert(t *testing.T) {
	reader := base64.NewDecoder(base64.StdEncoding, strings.NewReader(data))
	m, _, err := image.Decode(reader)
	if err != nil {
		panic(err)
	}

	res_b64 := string(Img2Ascii(ScaleImage(m, 80)))
	if res_b64 != expected {
		t.Fatalf("expected \n%s\n got \n%s\n", expected, res_b64)
	}

	res_f := string(MustFile2Ascii("example.jpg", 80))
	if res_f != res_b64 {
		t.Fatalf("expected \n%s\n got \n%s\n", res_b64, res_f)
	}

}

